a = (2, 5, 4)
b = 5, 8, 1, 2
c = b + a
print(c)
print('print(len(c)) -> ', len(c))
print('print(c.count(5)) -> ', c.count(5))
print('print(c.count(9)) -> ', c.count(9))
print('print(c.index(8)) -> ', c.index(8))
print('print(c.index(4)) -> ', c.index(4))
print('print(c.index(2)) -> ', c.index(2))
print('print(c.index(5)) -> ', c.index(5))
print('5 depois da posição 0'
      ' -> print(c.index(5, 0)) -> ', c.index(5, 0))
print('5 depois da posição 2'
      ' -> print(c.index(5, 2)) -> ', c.index(5, 2))
print('5 entre as posições 2 e 6'
      ' -> print(c.index(5, 2, 6)) -> ', c.index(5, 2, 6))
